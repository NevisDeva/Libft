#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int tmp;

	tmp = n;
	if (n < 0)
	{
		ft_putchar('-');
		tmp = -n;
	}
	if (tmp > 9)
	{
		ft_putnbr(tmp / 10);
		ft_putnbr(tmp % 10);
	}
	else
		ft_putchar(n + '0');
}
